package com.vicero13.gruzi_v0.pojo;

public class Car {
    private String model;
    private int lengthCarcase;
    private int widthCarcase;
    private int heightCarcase;
    private int capacityCarcase;
    private int maxWeightCarcase;
    private String numberplate;

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getLengthCarcase() {
        return lengthCarcase;
    }

    public void setLengthCarcase(int lengthCarcase) {
        this.lengthCarcase = lengthCarcase;
    }

    public int getWidthCarcase() {
        return widthCarcase;
    }

    public void setWidthCarcase(int widthCarcase) {
        this.widthCarcase = widthCarcase;
    }

    public int getHeightCarcase() {
        return heightCarcase;
    }

    public void setHeightCarcase(int heightCarcase) {
        this.heightCarcase = heightCarcase;
    }

    public int getCapacityCarcase() {
        return capacityCarcase;
    }

    public void setCapacityCarcase(int capacityCarcase) {
        this.capacityCarcase = capacityCarcase;
    }

    public int getMaxWeightCarcase() {
        return maxWeightCarcase;
    }

    public void setMaxWeightCarcase(int maxWeightCarcase) {
        this.maxWeightCarcase = maxWeightCarcase;
    }

    public String getNumberplate() {
        return numberplate;
    }

    public void setNumberplate(String numberplate) {
        this.numberplate = numberplate;
    }
}
